from .sudoku_env import SudokuEnv

def test_action_tuple():
    sudoku = SudokuEnv(3, 0.75)
    assert sudoku._actionToActionTuple(0) == (0,0, 1)
    assert sudoku._actionToActionTuple(sudoku.board_width**3 - 1) == (sudoku.board_width-1, sudoku.board_width-1, sudoku.board_width)
    assert sudoku._actionToActionTuple(sudoku.board_width**2) == (1,0, 1)
    assert sudoku._actionToActionTuple(sudoku.board_width**2 + sudoku.board_width) == (1,1,1)
    assert sudoku._actionToActionTuple(sudoku.board_width**2 + sudoku.board_width -1) == (1,0,sudoku.board_width)

def test_tuple():
    sudoku = SudokuEnv(3, 0.75)

    assert sudoku._actionTupleToAction(sudoku._actionToActionTuple(0)) == 0
    assert sudoku._actionTupleToAction(sudoku._actionToActionTuple(sudoku.board_width)) == sudoku.board_width
    assert sudoku._actionTupleToAction(sudoku._actionToActionTuple(sudoku.board_width**2)) == sudoku.board_width**2
    assert sudoku._actionTupleToAction(sudoku._actionToActionTuple(sudoku.board_width**2 + 1)) == sudoku.board_width**2 + 1


